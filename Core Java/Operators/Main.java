public class Main {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;

        boolean isStudent = true;
        boolean hasCertificate = false;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        System.out.println(a > b);
        System.out.println(a == b);
        System.out.println(a != b);

        System.out.println(isStudent && hasCertificate);
        System.out.println(isStudent || hasCertificate);
        System.out.println(!isStudent);

    }
}
