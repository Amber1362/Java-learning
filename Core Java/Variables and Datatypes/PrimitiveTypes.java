public class PrimitiveTypes {
    public static void main(String[] args) {
       
        byte age = 22;
        short noOfStudents = 28132;
        int populationOfDehradun = 12000000;
        long worldPopulation = 8000000000L;
        float price = 89.96f;
        double percentile = 65.2;
        char grade = 'A';
        boolean isPlaced = true;

        System.out.println("Age: " + age);
        System.out.println("Number of students: " + noOfStudents);
        System.out.println("Population of Dehradun: " + populationOfDehradun);
        System.out.println("World population: " + worldPopulation);
        System.out.println("Price: " + price);
        System.out.println("Percentile: " + percentile);
        System.out.println("Grade: " + grade);
        System.out.println("Placed: " + isPlaced);

    }
}